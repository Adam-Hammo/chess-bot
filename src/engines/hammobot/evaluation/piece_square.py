ps_map = [None]*33
for n_pieces in range(32):
    n_pieces+=1
    ps_map[n_pieces] = {}
    for pt in mg_ps_map.keys():
        eg_weight = min((32-n_pieces)/(32-10),1)
        mg_weight = 1-eg_weight
        ps_map[n_pieces][pt] = eg_weight*eg_ps_map[pt] + mg_weight*mg_ps_map[pt]

def get_piece_square_value(board, piece, colour, n_pieces) :
    square_set = board.pieces(piece, colour)
    if len(square_set)==0 :
        return 0
    if colour == chess.WHITE :
        return sum(ps_map[n_pieces][piece][63-np.array(list(square_set))])
    else :
        return sum(ps_map[n_pieces][piece][np.array(list(square_set))])

def evaluate_piece_square_difference(board, n_pieces) :
    white_points = (
        get_piece_square_value(board, chess.PAWN, chess.WHITE, n_pieces) +
        get_piece_square_value(board, chess.KNIGHT, chess.WHITE, n_pieces) +
        get_piece_square_value(board, chess.BISHOP, chess.WHITE, n_pieces) +
        get_piece_square_value(board, chess.ROOK, chess.WHITE, n_pieces) +
        get_piece_square_value(board, chess.QUEEN, chess.WHITE, n_pieces) +
        get_piece_square_value(board, chess.KING, chess.WHITE, n_pieces)
    )
    black_points = (
        get_piece_square_value(board, chess.PAWN, chess.BLACK, n_pieces) +
        get_piece_square_value(board, chess.KNIGHT, chess.BLACK, n_pieces) +
        get_piece_square_value(board, chess.BISHOP, chess.BLACK, n_pieces) +
        get_piece_square_value(board, chess.ROOK, chess.BLACK, n_pieces) +
        get_piece_square_value(board, chess.QUEEN, chess.BLACK, n_pieces) +
        get_piece_square_value(board, chess.KING, chess.BLACK, n_pieces)
    )
    return 1-white_points/black_points if black_points != 0 else 1